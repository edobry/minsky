/**
 * MemoryService unit tests (mt#1006)
 *
 * Uses:
 *  - An in-memory fake DB (satisfies MemoryServiceDb without `as unknown` casts)
 *  - MemoryVectorStorage (in-memory) for vector operations
 *  - A deterministic mock EmbeddingService
 *
 * Covers:
 *  - create → get round-trip with embedding populated
 *  - search returns semantic neighbors (record 2 ranked first for a query near its vector)
 *  - supersede chain: A→B, A.superseded_by = B.id, list(excludeSuperseded:true) excludes A
 *  - degraded-mode: EmbeddingService throws → create still succeeds, search returns {backend:"none",degraded:true}
 *  - list filters: list({type:"feedback", scope:"user"}) returns only matching records
 */

import { describe, it, expect, beforeEach } from "bun:test";
import { PgDialect } from "drizzle-orm/pg-core";
import { MemoryService, type MemoryServiceDb } from "../../../src/domain/memory/memory-service";
import { MemoryVectorStorage } from "../../../src/domain/storage/vector/memory-vector-storage";
import type { MemoryRecord } from "../../../src/domain/memory/types";

// ── Deterministic mock embedding service ─────────────────────────────────────

const DIMENSIONS = 4;

function makeVector(seed: number): number[] {
  return [seed, seed * 2, seed * 3, seed * 4];
}

const mockEmbeddingService = {
  async generateEmbedding(text: string): Promise<number[]> {
    // Deterministic: hash the text to a seed so different content → different vectors.
    const seed = text.split("").reduce((a, c) => a + c.charCodeAt(0), 0) % 100;
    return makeVector(seed);
  },
  async generateEmbeddings(texts: string[]): Promise<number[][]> {
    return Promise.all(texts.map((t) => mockEmbeddingService.generateEmbedding(t)));
  },
};

const throwingEmbeddingService = {
  async generateEmbedding(_text: string): Promise<number[]> {
    throw new Error("Embedding service unavailable");
  },
  async generateEmbeddings(_texts: string[]): Promise<number[][]> {
    throw new Error("Embedding service unavailable");
  },
};

// ── Minimal in-memory fake DB ─────────────────────────────────────────────────
//
// Strategy: use PgDialect.sqlToQuery() to render the Drizzle SQL condition to
// a plain string + params (e.g. `"memories"."type" = $1 and ...`, ["feedback"]).
// Then apply a simple evaluator against the row using that rendered query.
// This avoids brittle AST introspection while still exercising the real service code.

type MemoryRow = {
  id: string;
  type: string;
  name: string;
  description: string;
  content: string;
  scope: string;
  project_id: string | null;
  tags: string[];
  source_agent_id: string | null;
  source_session_id: string | null;
  confidence: number | null;
  superseded_by: string | null;
  created_at: Date;
  updated_at: Date;
  last_accessed_at: Date | null;
  access_count: number;
};

const pgDialect = new PgDialect();

/**
 * Evaluate a rendered WHERE clause + params against a single row.
 *
 * Handles the patterns generated by MemoryService:
 *   "memories"."col" = $N
 *   "memories"."col" is null
 *   "memories"."col" in ($N, $M, ...)
 *   (cond1 and cond2)                — parenthesised AND
 */
function evalSqlWhere(sql: string, params: unknown[], row: MemoryRow): boolean {
  // Strip outer parens added by `and()`
  let s = sql.trim();
  if (s.startsWith("(") && s.endsWith(")")) s = s.slice(1, -1).trim();

  // Split on top-level ` and ` (not inside parens)
  const parts = splitTopLevelAnd(s);
  if (parts.length > 1) {
    return parts.every((p) => evalSqlWhere(p, params, row));
  }

  // Pattern: "memories"."colname" = $N
  const eqMatch = /^"memories"\."(\w+)" = \$(\d+)$/.exec(s.trim());
  if (eqMatch) {
    const colName = eqMatch[1] as keyof MemoryRow;
    const paramIdx = Number(eqMatch[2]) - 1;
    return row[colName] === params[paramIdx];
  }

  // Pattern: "memories"."colname" is null
  const isNullMatch = /^"memories"\."(\w+)" is null$/.exec(s.trim());
  if (isNullMatch) {
    const colName = isNullMatch[1] as keyof MemoryRow;
    return row[colName] === null;
  }

  // Pattern: "memories"."colname" in ($N, $M, ...)
  const inMatch = /^"memories"\."(\w+)" in \(([^)]+)\)$/.exec(s.trim());
  if (inMatch && inMatch[1] && inMatch[2]) {
    const colName = inMatch[1] as keyof MemoryRow;
    const paramNums = inMatch[2]
      .split(",")
      .map((p) => Number(p.trim().slice(1)) - 1)
      .map((i) => params[i]);
    return paramNums.includes(row[colName]);
  }

  // Unknown — pass through (permissive for test purposes)
  return true;
}

/** Split a SQL string on ` and ` at the top level (outside parentheses). */
function splitTopLevelAnd(sql: string): string[] {
  const parts: string[] = [];
  let depth = 0;
  let start = 0;
  for (let i = 0; i < sql.length; i++) {
    if (sql[i] === "(") depth++;
    else if (sql[i] === ")") depth--;
    else if (depth === 0 && sql.slice(i, i + 5) === " and ") {
      parts.push(sql.slice(start, i).trim());
      start = i + 5;
    }
  }
  parts.push(sql.slice(start).trim());
  return parts;
}

let idCounter = 1;
function genId(): string {
  return `mem-${String(idCounter++).padStart(4, "0")}`;
}

function createFakeDb(initialRows: MemoryRow[] = []): MemoryServiceDb & {
  _rows: Map<string, MemoryRow>;
} {
  const rows = new Map<string, MemoryRow>(initialRows.map((r) => [r.id, r]));

  function queryRows(cond?: any): MemoryRow[] {
    const all = Array.from(rows.values());
    if (!cond) return all;
    const { sql, params } = pgDialect.sqlToQuery(cond);
    return all.filter((r) => evalSqlWhere(sql, params, r));
  }

  const fakeDb: MemoryServiceDb & { _rows: Map<string, MemoryRow> } = {
    _rows: rows,

    select(_fields?: unknown) {
      return {
        from(_table: unknown) {
          return {
            where(cond: any) {
              return Promise.resolve(queryRows(cond));
            },
            then(resolve: (v: MemoryRow[]) => void) {
              resolve(queryRows());
            },
          };
        },
      };
    },

    insert(_table: unknown) {
      return {
        values(data: Record<string, any>) {
          const id = (data["id"] as string | undefined) ?? genId();
          const row: MemoryRow = {
            id,
            type: data["type"] ?? "user",
            name: data["name"] ?? "",
            description: data["description"] ?? "",
            content: data["content"] ?? "",
            scope: data["scope"] ?? "user",
            project_id: data["projectId"] ?? data["project_id"] ?? null,
            tags: data["tags"] ?? [],
            source_agent_id: data["sourceAgentId"] ?? data["source_agent_id"] ?? null,
            source_session_id: data["sourceSessionId"] ?? data["source_session_id"] ?? null,
            confidence: data["confidence"] ?? null,
            superseded_by: data["supersededBy"] ?? data["superseded_by"] ?? null,
            created_at: new Date(),
            updated_at: new Date(),
            last_accessed_at: null,
            access_count: 0,
          };
          rows.set(id, row);
          return {
            returning() {
              return Promise.resolve([row]);
            },
          };
        },
      };
    },

    update(_table: unknown) {
      return {
        set(data: Record<string, any>) {
          return {
            where(cond: any) {
              return {
                returning() {
                  const matched = queryRows(cond);
                  const updated: MemoryRow[] = [];
                  for (const row of matched) {
                    const next: MemoryRow = {
                      ...row,
                      ...(data["type"] !== undefined ? { type: data["type"] as string } : {}),
                      ...(data["name"] !== undefined ? { name: data["name"] as string } : {}),
                      ...(data["description"] !== undefined
                        ? { description: data["description"] as string }
                        : {}),
                      ...(data["content"] !== undefined
                        ? { content: data["content"] as string }
                        : {}),
                      ...(data["scope"] !== undefined ? { scope: data["scope"] as string } : {}),
                      ...("projectId" in data
                        ? { project_id: data["projectId"] as string | null }
                        : {}),
                      ...(data["tags"] !== undefined ? { tags: data["tags"] as string[] } : {}),
                      ...("sourceAgentId" in data
                        ? { source_agent_id: data["sourceAgentId"] as string | null }
                        : {}),
                      ...("sourceSessionId" in data
                        ? { source_session_id: data["sourceSessionId"] as string | null }
                        : {}),
                      ...("confidence" in data
                        ? { confidence: data["confidence"] as number | null }
                        : {}),
                      ...("supersededBy" in data
                        ? { superseded_by: data["supersededBy"] as string | null }
                        : {}),
                      updated_at: new Date(),
                    };
                    rows.set(row.id, next);
                    updated.push(next);
                  }
                  return Promise.resolve(updated);
                },
              };
            },
          };
        },
      };
    },

    delete(_table: unknown) {
      return {
        where(cond: any) {
          const matched = queryRows(cond);
          for (const r of matched) rows.delete(r.id);
          return Promise.resolve();
        },
      };
    },

    transaction<T>(fn: (tx: MemoryServiceDb) => Promise<T>): Promise<T> {
      // Run the function with the same db as the transaction (no rollback needed for tests).
      return fn(fakeDb);
    },
  };

  return fakeDb;
}

// ── Helpers ───────────────────────────────────────────────────────────────────

function makeService(
  db: MemoryServiceDb,
  vectorStorage: MemoryVectorStorage,
  embeddingService = mockEmbeddingService
): MemoryService {
  return new MemoryService({ db, vectorStorage, embeddingService });
}

function makeInput(overrides: Partial<Parameters<MemoryService["create"]>[0]> = {}) {
  return {
    type: "user" as const,
    name: "Test memory",
    description: "A test memory",
    content: "This is test content",
    scope: "user" as const,
    ...overrides,
  };
}

// ── Tests ─────────────────────────────────────────────────────────────────────

describe("MemoryService", () => {
  let db: ReturnType<typeof createFakeDb>;
  let vectorStorage: MemoryVectorStorage;
  let service: MemoryService;

  beforeEach(() => {
    idCounter = 1;
    db = createFakeDb();
    vectorStorage = new MemoryVectorStorage(DIMENSIONS);
    service = makeService(db, vectorStorage);
  });

  // ── create → get round-trip ────────────────────────────────────────────────

  describe("create → get round-trip", () => {
    it("inserts a memory row and returns a record with the correct fields", async () => {
      const record = await service.create(
        makeInput({ name: "Hello", content: "world", type: "feedback", scope: "project" })
      );

      expect(record.id).toBeDefined();
      expect(record.name).toBe("Hello");
      expect(record.content).toBe("world");
      expect(record.type).toBe("feedback");
      expect(record.scope).toBe("project");
      expect(record.supersededBy).toBeNull();
    });

    it("get() returns the created record by id", async () => {
      const created = await service.create(makeInput());
      const fetched = await service.get(created.id);

      expect(fetched).not.toBeNull();
      if (!fetched) throw new Error("fetched is null");
      expect(fetched.id).toBe(created.id);
      expect(fetched.name).toBe(created.name);
    });

    it("stores an embedding after create (verified via vector search)", async () => {
      const created = await service.create(makeInput({ content: "unique content for embedding" }));

      // Verify the embedding was stored by querying the vector store directly.
      const queryVec = await mockEmbeddingService.generateEmbedding("unique content for embedding");
      const results = await vectorStorage.search(queryVec, { limit: 5 });

      expect(results.some((r) => r.id === created.id)).toBe(true);
    });

    it("get() returns null for an unknown id", async () => {
      const result = await service.get("nonexistent-id");
      expect(result).toBeNull();
    });
  });

  // ── search: semantic neighbors ─────────────────────────────────────────────

  describe("search", () => {
    it("returns results ordered by vector similarity (closest record first)", async () => {
      // Create two records with distinctly different content so they get different vectors.
      await service.create(makeInput({ name: "Record 1", content: "apple banana" }));
      const rec2 = await service.create(
        makeInput({ name: "Record 2", content: "zebra xenon yacht" })
      );

      // Search with a query matching rec2's content exactly
      const response = await service.search("zebra xenon yacht", { limit: 5 });

      expect(response.backend).toBe("embeddings");
      expect(response.degraded).toBe(false);
      expect(response.results.length).toBeGreaterThan(0);

      // rec2 should rank first (score 0 = exact match)
      const firstResult = response.results[0];
      expect(firstResult).toBeDefined();
      if (!firstResult) throw new Error("firstResult is undefined");
      expect(firstResult.record.id).toBe(rec2.id);
    });

    it("returns an empty results array when no memories exist", async () => {
      const response = await service.search("anything");

      expect(response.degraded).toBe(false);
      expect(response.results).toHaveLength(0);
    });
  });

  // ── supersede chain ────────────────────────────────────────────────────────

  describe("supersede", () => {
    it("marks old memory as superseded and inserts the replacement", async () => {
      const old = await service.create(makeInput({ name: "Old knowledge", content: "old stuff" }));

      const { old: updatedOld, replacement } = await service.supersede(
        old.id,
        makeInput({ name: "New knowledge", content: "new stuff" }),
        "Updated with fresh info"
      );

      expect(updatedOld.supersededBy).toBe(replacement.id);
      expect(replacement.supersededBy).toBeNull();

      // Verify via get()
      const refetched = await service.get(old.id);
      expect(refetched).not.toBeNull();
      if (!refetched) throw new Error("refetched is null");
      expect(refetched.supersededBy).toBe(replacement.id);
    });

    it("list({excludeSuperseded:true}) excludes superseded memories", async () => {
      const memA = await service.create(makeInput({ name: "Memory A", content: "old content" }));
      const { replacement: memB } = await service.supersede(
        memA.id,
        makeInput({ name: "Memory B", content: "new content" })
      );

      const active = await service.list({ excludeSuperseded: true });
      const activeIds = active.map((r: MemoryRecord) => r.id);

      expect(activeIds).not.toContain(memA.id);
      expect(activeIds).toContain(memB.id);
    });

    it("list() without excludeSuperseded returns ALL memories", async () => {
      const memA = await service.create(makeInput({ name: "Memory A", content: "old" }));
      const { replacement: memB } = await service.supersede(
        memA.id,
        makeInput({ name: "Memory B", content: "new" })
      );

      const all = await service.list();
      const allIds = all.map((r: MemoryRecord) => r.id);

      expect(allIds).toContain(memA.id);
      expect(allIds).toContain(memB.id);
    });
  });

  // ── degraded mode ──────────────────────────────────────────────────────────

  describe("degraded mode", () => {
    beforeEach(() => {
      service = makeService(db, vectorStorage, throwingEmbeddingService);
    });

    it("create() still succeeds when EmbeddingService throws", async () => {
      const record = await service.create(makeInput({ content: "will fail to embed" }));

      expect(record.id).toBeDefined();
      expect(record.name).toBe("Test memory");
    });

    it("search() returns {backend:'none', degraded:true} when EmbeddingService throws", async () => {
      // First create a record via a working service so there's something to find
      const workingService = makeService(db, vectorStorage, mockEmbeddingService);
      await workingService.create(makeInput());

      // Now search with the failing service
      const response = await service.search("anything");

      expect(response.backend).toBe("none");
      expect(response.degraded).toBe(true);
      expect(response.results).toHaveLength(0);
    });
  });

  // ── list filters ───────────────────────────────────────────────────────────

  describe("list filters", () => {
    beforeEach(async () => {
      await service.create(
        makeInput({ name: "User/project", type: "user", scope: "project", projectId: "proj-a" })
      );
      await service.create(makeInput({ name: "Feedback/user", type: "feedback", scope: "user" }));
      await service.create(
        makeInput({ name: "Project/cross", type: "project", scope: "cross_project" })
      );
      await service.create(makeInput({ name: "Feedback/user 2", type: "feedback", scope: "user" }));
    });

    it("filters by type", async () => {
      const results = await service.list({ type: "feedback" });
      expect(results.length).toBe(2);
      results.forEach((r: MemoryRecord) => expect(r.type).toBe("feedback"));
    });

    it("filters by scope", async () => {
      const results = await service.list({ scope: "user" });
      expect(results.length).toBe(2);
      results.forEach((r: MemoryRecord) => expect(r.scope).toBe("user"));
    });

    it("filters by type AND scope together", async () => {
      const results = await service.list({ type: "feedback", scope: "user" });
      expect(results.length).toBe(2);
      results.forEach((r: MemoryRecord) => {
        expect(r.type).toBe("feedback");
        expect(r.scope).toBe("user");
      });
    });

    it("returns all when no filter is given", async () => {
      const results = await service.list();
      expect(results.length).toBe(4);
    });
  });

  // ── update ────────────────────────────────────────────────────────────────

  describe("update", () => {
    it("updates mutable fields and returns the updated record", async () => {
      const created = await service.create(makeInput({ name: "Original" }));
      const updated = await service.update(created.id, { name: "Updated", tags: ["foo", "bar"] });

      expect(updated).not.toBeNull();
      if (!updated) throw new Error("updated is null");
      expect(updated.name).toBe("Updated");
      expect(updated.tags).toEqual(["foo", "bar"]);
    });

    it("returns null when updating a non-existent id", async () => {
      const result = await service.update("does-not-exist", { name: "Nope" });
      expect(result).toBeNull();
    });
  });

  // ── delete ────────────────────────────────────────────────────────────────

  describe("delete", () => {
    it("removes the record from the store", async () => {
      const created = await service.create(makeInput());
      await service.delete(created.id);

      const fetched = await service.get(created.id);
      expect(fetched).toBeNull();
    });
  });
});
