/**
 * Rules Compile Monolithic
 *
 * Compiles rules into a single monolithic file (e.g., AGENTS.md) by
 * selecting always-on rules, stripping frontmatter, and organizing into sections.
 */

import { join } from "path";
import { RuleService } from "../rules";
import { classifyRuleType, RuleType } from "./rule-classifier";
import type { Rule } from "./types";

export interface CompileTarget {
  type: "agents.md" | "claude.md";
  outputPath?: string; // defaults based on type
}

export interface CompileOptions {
  target: CompileTarget;
  dryRun?: boolean;
  sectionMapping?: Record<string, string[]>; // section name → rule IDs
  workspacePath: string;
}

export interface CompileResult {
  content: string;
  rulesIncluded: string[];
  rulesSkipped: string[];
  outputPath: string;
  dryRun: boolean;
}

/**
 * Default section mapping for AGENTS.md target
 */
const DEFAULT_AGENTS_MD_SECTIONS: Record<string, string[]> = {
  "Build & Test": ["bun_over_node", "tests"],
  "Code Style": ["comments", "constants-management", "ensure-ascii-code-symbols"],
  Architecture: ["domain-oriented-modules", "command-organization"],
  Testing: ["designing-tests"],
  "Minsky Workflow": ["minsky-workflow", "minsky-workflow-orchestrator"],
  "Git & PR Workflow": ["git-usage-policy", "pr-description-guidelines", "pr-preparation-workflow"],
  Boundaries: ["operational-safety-dry-run-first", "terminal-command-best-practices"],
};

/**
 * Get the default output path for a compile target type
 */
function getDefaultOutputPath(targetType: CompileTarget["type"], workspacePath: string): string {
  switch (targetType) {
    case "agents.md":
      return join(workspacePath, "AGENTS.md");
    case "claude.md":
      return join(workspacePath, "CLAUDE.md");
    default:
      return join(workspacePath, "AGENTS.md");
  }
}

/**
 * Compiles rules into a monolithic output file.
 *
 * 1. Lists all rules via the service
 * 2. Classifies each rule
 * 3. Selects rules that are in sectionMapping or are alwaysApply
 * 4. Organizes them into sections
 * 5. Returns the compiled content and metadata
 */
export async function compileMonolithic(
  ruleService: RuleService,
  options: CompileOptions
): Promise<CompileResult> {
  const { target, dryRun = false, workspacePath } = options;

  const sectionMapping = options.sectionMapping || DEFAULT_AGENTS_MD_SECTIONS;

  // Resolve the output path
  const outputPath = target.outputPath || getDefaultOutputPath(target.type, workspacePath);

  // List all rules
  const allRules = await ruleService.listRules({});

  // Build a lookup map by rule ID
  const ruleMap = new Map<string, Rule>();
  for (const rule of allRules) {
    ruleMap.set(rule.id, rule);
  }

  // Build set of all explicitly mapped rule IDs
  const mappedRuleIds = new Set<string>();
  for (const ruleIds of Object.values(sectionMapping)) {
    for (const id of ruleIds) {
      mappedRuleIds.add(id);
    }
  }

  // Track inclusion
  const rulesIncluded: string[] = [];
  const rulesSkipped: string[] = [];

  // Collect rules by section
  const sectionContents: Array<{ sectionName: string; rules: Rule[] }> = [];

  for (const [sectionName, ruleIds] of Object.entries(sectionMapping)) {
    const sectionRules: Rule[] = [];
    for (const ruleId of ruleIds) {
      const rule = ruleMap.get(ruleId);
      if (rule) {
        sectionRules.push(rule);
        rulesIncluded.push(ruleId);
      }
      // If rule not found, it just won't appear (silently skip missing rules)
    }
    if (sectionRules.length > 0) {
      sectionContents.push({ sectionName, rules: sectionRules });
    }
  }

  // Collect alwaysApply rules not already in a section
  const generalRules: Rule[] = [];
  for (const rule of allRules) {
    if (!mappedRuleIds.has(rule.id)) {
      const ruleType = classifyRuleType(rule);
      if (ruleType === RuleType.ALWAYS_APPLY) {
        generalRules.push(rule);
        rulesIncluded.push(rule.id);
      }
    }
  }

  // Track skipped rules (those not included)
  for (const rule of allRules) {
    if (!rulesIncluded.includes(rule.id)) {
      rulesSkipped.push(rule.id);
    }
  }

  // Build the output string
  const lines: string[] = [];

  lines.push("<!-- Generated by minsky rules compile. Do not edit directly. -->");
  lines.push("");
  lines.push("# Project Instructions");
  lines.push("");

  for (const { sectionName, rules } of sectionContents) {
    lines.push(`## ${sectionName}`);
    lines.push("");
    for (const rule of rules) {
      const content = rule.content.trim();
      if (content) {
        lines.push(content);
        lines.push("");
      }
    }
  }

  if (generalRules.length > 0) {
    lines.push("## General");
    lines.push("");
    for (const rule of generalRules) {
      const content = rule.content.trim();
      if (content) {
        lines.push(content);
        lines.push("");
      }
    }
  }

  const content = lines.join("\n");

  return {
    content,
    rulesIncluded,
    rulesSkipped,
    outputPath,
    dryRun,
  };
}
