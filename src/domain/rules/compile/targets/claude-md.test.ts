import { describe, it, expect } from "bun:test";
import { claudeMdTarget, buildClaudeMdContent } from "./claude-md";
import {
  ALWAYS_APPLY_CONTENT,
  MANUAL_RULE_CONTENT,
  GLOB_RULE_CONTENT,
  PROJECT_INSTRUCTIONS_HEADER,
} from "./test-fixtures";
import { makeRule } from "../test-utils";

describe("claude-md target: listOutputFiles()", () => {
  it("returns single path at <workspace>/CLAUDE.md", () => {
    const paths = claudeMdTarget.listOutputFiles([], {}, "/workspace");
    expect(paths).toHaveLength(1);
    expect(paths[0]).toBe("/workspace/CLAUDE.md");
  });
});

describe("claude-md target: buildClaudeMdContent()", () => {
  describe("basic compilation", () => {
    it("output starts with the generation comment", () => {
      const { content } = buildClaudeMdContent([]);
      expect(content).toContain(
        "<!-- Generated by minsky rules compile. Do not edit directly. -->"
      );
      expect(content.startsWith("<!-- Generated by minsky rules compile.")).toBe(true);
    });

    it("output has # Project Instructions header", () => {
      const { content } = buildClaudeMdContent([]);
      expect(content).toContain(PROJECT_INSTRUCTIONS_HEADER);
    });
  });

  describe("only-alwaysApply default", () => {
    it("only includes ALWAYS_APPLY rules by default", () => {
      const rules = [
        makeRule("always-rule", ALWAYS_APPLY_CONTENT, { alwaysApply: true }),
        makeRule("manual-rule", MANUAL_RULE_CONTENT),
        makeRule("glob-rule", GLOB_RULE_CONTENT, { globs: ["**/*.ts"] }),
        makeRule("agent-rule", "Agent rule content", { description: "An agent rule" }),
      ];

      const { content, rulesIncluded, rulesSkipped } = buildClaudeMdContent(rules);

      expect(content).toContain(ALWAYS_APPLY_CONTENT);
      expect(content).not.toContain(MANUAL_RULE_CONTENT);
      expect(content).not.toContain(GLOB_RULE_CONTENT);
      expect(content).not.toContain("Agent rule content");

      expect(rulesIncluded).toContain("always-rule");
      expect(rulesSkipped).toContain("manual-rule");
      expect(rulesSkipped).toContain("glob-rule");
      expect(rulesSkipped).toContain("agent-rule");
    });

    it("includes multiple alwaysApply rules", () => {
      const rules = [
        makeRule("always-1", "Always content 1", { alwaysApply: true }),
        makeRule("always-2", "Always content 2", { alwaysApply: true }),
        makeRule("manual-rule", "Manual content"),
      ];

      const { content, rulesIncluded, rulesSkipped } = buildClaudeMdContent(rules);

      expect(content).toContain("Always content 1");
      expect(content).toContain("Always content 2");
      expect(content).not.toContain("Manual content");

      expect(rulesIncluded).toContain("always-1");
      expect(rulesIncluded).toContain("always-2");
      expect(rulesSkipped).toContain("manual-rule");
    });

    it("returns empty rulesIncluded when no alwaysApply rules exist", () => {
      const rules = [
        makeRule("manual-rule", MANUAL_RULE_CONTENT),
        makeRule("glob-rule", GLOB_RULE_CONTENT, { globs: ["**/*.ts"] }),
      ];

      const { rulesIncluded, rulesSkipped } = buildClaudeMdContent(rules);

      expect(rulesIncluded).toHaveLength(0);
      expect(rulesSkipped).toContain("manual-rule");
      expect(rulesSkipped).toContain("glob-rule");
    });

    it("returns empty arrays for empty input", () => {
      const { rulesIncluded, rulesSkipped } = buildClaudeMdContent([]);

      expect(rulesIncluded).toHaveLength(0);
      expect(rulesSkipped).toHaveLength(0);
    });
  });

  describe("output format", () => {
    it("always-apply rules appear directly in the output without section headers", () => {
      const rules = [makeRule("always-rule", ALWAYS_APPLY_CONTENT, { alwaysApply: true })];

      const { content } = buildClaudeMdContent(rules);

      // No section headers (no ##) except the main # Project Instructions
      const lines = content.split("\n");
      const sectionHeaders = lines.filter((l) => l.startsWith("## "));
      expect(sectionHeaders).toHaveLength(0);
    });
  });
});
