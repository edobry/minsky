import {
  pgTable,
  text,
  integer,
  timestamp,
  jsonb,
  boolean,
  primaryKey,
  index,
} from "drizzle-orm/pg-core";
import { sql } from "drizzle-orm";
import { vector, customType } from "drizzle-orm/pg-core";
import { agentTranscriptsTable } from "./agent-transcripts-schema";

/**
 * Custom tsvector type for Postgres full-text search.
 *
 * Drizzle ORM does not have a built-in tsvector column type. We declare it
 * as a custom type so Drizzle can model the generated column without trying
 * to serialize/deserialize the value from user-space code.
 */
const tsvector = customType<{ data: string }>({
  dataType() {
    return "tsvector";
  },
});

/**
 * Agent transcript turns table — per-turn rows extracted from agent session transcripts.
 *
 * Each row holds one conversation turn: the user prompt, the assistant response,
 * any tool calls made, timing information, a vector embedding for semantic search,
 * and a generated tsvector column for Postgres FTS.
 *
 * Spawn-boundary turns (where the agent invoked a subagent via the Agent tool) are
 * flagged with is_spawn_boundary = true. Their tool-result payload is excluded from
 * assistant_text, embedding, and fts_text to avoid 2x storage of subagent content.
 *
 * @see mt#1313 — Transcript search: harness-agnostic ingestion
 * @see mt#1324 — Foundation schema migration
 */
export const agentTranscriptTurnsTable = pgTable(
  "agent_transcript_turns",
  {
    agentSessionId: text("agent_session_id")
      .notNull()
      .references(() => agentTranscriptsTable.agentSessionId),

    turnIndex: integer("turn_index").notNull(),

    // Turn content
    userText: text("user_text"),
    assistantText: text("assistant_text"),
    toolCalls: jsonb("tool_calls"),

    // Turn timing
    startedAt: timestamp("started_at", { withTimezone: true }),
    endedAt: timestamp("ended_at", { withTimezone: true }),

    // Vector embedding for semantic similarity search
    embedding: vector("embedding", { dimensions: 1536 }),

    // Full-text search column — generated by Postgres from user_text + assistant_text.
    // The GENERATED ALWAYS AS clause is applied in the migration SQL; Drizzle models
    // the column type here for query-building purposes only.
    ftsText: tsvector("fts_text").generatedAlwaysAs(
      sql`to_tsvector('english', coalesce(user_text, '') || ' ' || coalesce(assistant_text, ''))`
    ),

    // Subagent spawn boundary flag — true when this turn invoked a subagent via the Agent tool.
    // The tool-result payload for spawn-boundary turns is excluded from assistantText / embedding / ftsText.
    isSpawnBoundary: boolean("is_spawn_boundary").default(false),
  },
  (table) => [
    primaryKey({ columns: [table.agentSessionId, table.turnIndex] }),
    index("idx_agent_transcript_turns_fts").using("gin", table.ftsText),
    index("idx_agent_transcript_turns_embedding").using(
      "hnsw",
      table.embedding.asc().nullsLast().op("vector_l2_ops")
    ),
  ]
);
