// ---------------------------------------------------------------------------
// Helper: create a fake readLines function returning the given lines
// ---------------------------------------------------------------------------

function fakeReadLines(lines: string[]): () => Promise<string[]> {
  return async () => lines;
}

function fakeReadLinesError(msg: string): () => Promise<string[]> {
  return async () => {
    throw new Error(msg);
  };
}

