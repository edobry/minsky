describe("scanFileForBanner — file with HTML comment banner (canonical minsky output)", () => {
  it("detects the minsky rules compile banner", async () => {
    const lines = [MINSKY_BANNER, "# Content"];
    const result = await scanFileForBanner("/fake/CLAUDE.md", 5, fakeReadLines(lines));
    expect(result.found).toBe(true);
    expect(result.patternName).toContain("HTML comment");
    expect(result.matchedLine).toContain("Generated by");
  });
});

describe("scanFileForBanner — each banner form via injectable readLines", () => {
  it("detects HTML Do not edit banner", async () => {
    const lines = ["<!-- Do not edit this file. -->", "content"];
    const result = await scanFileForBanner("/fake/file.md", 5, fakeReadLines(lines));
    expect(result.found).toBe(true);
    expect(result.patternName).toBe(HTML_DO_NOT_EDIT_NAME);
  });

  it("detects AUTOGENERATED line comment", async () => {
    const lines = ["// AUTOGENERATED — do not edit", "import foo from 'bar';"];
    const result = await scanFileForBanner("/fake/file.ts", 5, fakeReadLines(lines));
    expect(result.found).toBe(true);
    expect(result.patternName).toBe(LINE_AUTOGENERATED_NAME);
  });

  it("detects block comment Generated by", async () => {
    const lines = ["/* Generated by openapi-generator */", "export const x = 1;"];
    const result = await scanFileForBanner("/fake/file.ts", 5, fakeReadLines(lines));
    expect(result.found).toBe(true);
    expect(result.patternName).toBe(BLOCK_GENERATED_BY_NAME);
  });

  it("detects hash comment Generated by", async () => {
    const lines = ["# Generated by minsky rules compile", "key: value"];
    const result = await scanFileForBanner("/fake/file.yaml", 5, fakeReadLines(lines));
    expect(result.found).toBe(true);
    expect(result.patternName).toBe(HASH_GENERATED_BY_NAME);
  });

  it("detects verbal form do not edit directly", async () => {
    const lines = ["do not edit directly — generated by tool", "content"];
    const result = await scanFileForBanner("/fake/file.md", 5, fakeReadLines(lines));
    expect(result.found).toBe(true);
    expect(result.patternName).toBe(VERBAL_FORM_NAME);
  });
});

describe("scanFileForBanner — negative: file with no banner", () => {
  it("permits a normal TypeScript file", async () => {
    const lines = ["import { foo } from './bar';", "", "const x = 42;"];
    const result = await scanFileForBanner("/fake/normal.ts", 5, fakeReadLines(lines));
    expect(result.found).toBe(false);
    expect(result.skipReason).toBeUndefined();
  });

  it("permits a file that starts with a shebang", async () => {
    const lines = [
      "#!/usr/bin/env bun",
      "// Normal hook file — not generated",
      "",
      "console.log('hi');",
    ];
    const result = await scanFileForBanner("/fake/hook.ts", 5, fakeReadLines(lines));
    expect(result.found).toBe(false);
  });

  it("permits an empty file", async () => {
    const result = await scanFileForBanner("/fake/empty.ts", 5, fakeReadLines([]));
    expect(result.found).toBe(false);
  });
});

describe("scanFileForBanner — negative: marker beyond line 5", () => {
  it("permits when the banner is on line 6", async () => {
    const lines = ["line 1", "line 2", "line 3", "line 4", "line 5", MINSKY_BANNER, "line 7"];
    // Only first 5 lines are checked; line 6 (index 5) contains the banner
    const result = await scanFileForBanner("/fake/file.md", 5, fakeReadLines(lines));
    expect(result.found).toBe(false);
  });

  it("permits when the AUTOGENERATED comment is on line 10", async () => {
    const lines = Array.from({ length: 9 }, (_, i) => `// regular line ${i + 1}`);
    lines.push("// AUTOGENERATED — do not modify");
    const result = await scanFileForBanner("/fake/file.ts", 5, fakeReadLines(lines));
    expect(result.found).toBe(false);
  });
});

describe("scanFileForBanner — file-read-error: fail-open", () => {
  it("returns found=false with skipReason on a read error", async () => {
    const result = await scanFileForBanner(
      "/fake/file.ts",
      5,
      fakeReadLinesError("permission denied")
    );
    expect(result.found).toBe(false);
    expect(result.skipReason).toContain("permission denied");
  });

  it("returns found=false for a nonexistent file (empty lines from readFirstLines)", async () => {
    // readFirstLines returns [] for nonexistent files — no lines, no match
    const result = await scanFileForBanner("/nonexistent/path/to/missing-file.ts", 5);
    expect(result.found).toBe(false);
  });
});

