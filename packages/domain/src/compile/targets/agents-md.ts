import { join } from "path";
import realFs from "fs/promises";
import { classifyRuleType, RuleType } from "../../rules/rule-classifier";
import type { Rule } from "../../rules/types";
import type { MinskyCompileTarget, MinskyTargetOptions, MinskyCompileFsDeps } from "../types";
import { evaluateSizeBudget } from "../size-budget";
import { DEFAULT_AGENTS_MD_SIZE_BUDGET } from "../agents-md-size-budget";
import type { MinskyMonolithicCompileResult } from "../size-budget-report";
import { loadAdaptedRules, type SkipLogFn, type DynamicImportFn } from "./rule-loader";

/** Matches the legacy `rules/compile/targets/agents-md.ts` banner exactly (see claude-md.ts module doc). */
const AGENTS_MD_BANNER = "<!-- Generated by minsky rules compile. Do not edit directly. -->";

// `DEFAULT_AGENTS_MD_SIZE_BUDGET` is imported (not declared here) so both
// this new-pipeline target and the legacy `agents-md.ts` share one threshold
// and cannot silently re-diverge (mt#3076 — see `agents-md-size-budget.ts`
// for the full rationale, including why the thresholds were raised well
// above the mt#2802 originals).
export { DEFAULT_AGENTS_MD_SIZE_BUDGET };

/**
 * Default section mapping for AGENTS.md, ported unchanged from the legacy
 * target (mt#2868 removed the two orphan "Minsky Workflow" entries there;
 * carried forward here as-is).
 */
export const DEFAULT_AGENTS_MD_SECTIONS: Record<string, string[]> = {
  "Build & Test": ["bun_over_node", "tests"],
  "Code Style": ["comments", "constants-management", "ensure-ascii-code-symbols"],
  Architecture: ["domain-oriented-modules", "architecture"],
  Testing: ["testing-standards", "test-infrastructure", "testing-boundaries"],
  "Git & PR Workflow": ["pr-description-guidelines"],
  Boundaries: ["operational-safety-dry-run-first", "terminal-command-best-practices"],
};

function agentsMdOutputPath(workspacePath: string): string {
  return join(workspacePath, "AGENTS.md");
}

/**
 * Build compiled content from rules and section mapping. Ports `buildContent`
 * from the legacy target 1:1 in behavior, operating on the legacy-shaped
 * `Rule[]` `loadAdaptedRules` produces.
 */
export function buildContent(
  allRules: Rule[],
  sectionMapping: Record<string, string[]>
): { content: string; rulesIncluded: string[]; rulesSkipped: string[] } {
  const ruleMap = new Map<string, Rule>();
  for (const rule of allRules) {
    ruleMap.set(rule.id, rule);
  }

  const mappedRuleIds = new Set<string>();
  for (const ruleIds of Object.values(sectionMapping)) {
    for (const id of ruleIds) {
      mappedRuleIds.add(id);
    }
  }

  const rulesIncluded: string[] = [];
  const sectionContents: Array<{ sectionName: string; rules: Rule[] }> = [];

  for (const [sectionName, ruleIds] of Object.entries(sectionMapping)) {
    const sectionRules: Rule[] = [];
    for (const ruleId of ruleIds) {
      const rule = ruleMap.get(ruleId);
      if (rule) {
        sectionRules.push(rule);
        rulesIncluded.push(ruleId);
      }
      // If rule not found, silently skip
    }
    if (sectionRules.length > 0) {
      sectionContents.push({ sectionName, rules: sectionRules });
    }
  }

  const generalRules: Rule[] = [];
  for (const rule of allRules) {
    if (!mappedRuleIds.has(rule.id)) {
      const ruleType = classifyRuleType(rule);
      if (ruleType === RuleType.ALWAYS_APPLY) {
        generalRules.push(rule);
        rulesIncluded.push(rule.id);
      }
    }
  }

  const rulesSkipped: string[] = [];
  for (const rule of allRules) {
    if (!rulesIncluded.includes(rule.id)) {
      rulesSkipped.push(rule.id);
    }
  }

  const lines: string[] = [];
  lines.push(AGENTS_MD_BANNER);
  lines.push("");
  lines.push("# Project Instructions");
  lines.push("");

  for (const { sectionName, rules } of sectionContents) {
    lines.push(`## ${sectionName}`);
    lines.push("");
    for (const rule of rules) {
      const content = rule.content.trim();
      if (content) {
        lines.push(content);
        lines.push("");
      }
    }
  }

  if (generalRules.length > 0) {
    lines.push("## General");
    lines.push("");
    for (const rule of generalRules) {
      const content = rule.content.trim();
      if (content) {
        lines.push(content);
        lines.push("");
      }
    }
  }

  return { content: lines.join("\n"), rulesIncluded, rulesSkipped };
}

/** Build the agents.md target, injecting fs/dynamicImport/skip-log for tests. */
function makeAgentsMdTarget(
  dynamicImport?: DynamicImportFn,
  onSkip?: SkipLogFn
): MinskyCompileTarget {
  return {
    id: "agents.md",
    displayName: "AGENTS.md",

    defaultOutputPath(workspacePath: string): string {
      return agentsMdOutputPath(workspacePath);
    },

    async listOutputFiles(
      options: MinskyTargetOptions,
      workspacePath: string,
      _fsDeps?: MinskyCompileFsDeps
    ): Promise<string[]> {
      return [options.outputPath || agentsMdOutputPath(workspacePath)];
    },

    async compile(
      options: MinskyTargetOptions,
      workspacePath: string,
      fsDeps?: MinskyCompileFsDeps
    ): Promise<MinskyMonolithicCompileResult> {
      const fs = fsDeps ?? (realFs as MinskyCompileFsDeps);
      const rules = await loadAdaptedRules(workspacePath, fs, onSkip, dynamicImport);

      const { content, rulesIncluded, rulesSkipped } = buildContent(
        rules,
        DEFAULT_AGENTS_MD_SECTIONS
      );

      const sizeEvaluation = evaluateSizeBudget({
        sizeChars: content.length,
        rules,
        includedIds: rulesIncluded,
        defaultBudget: DEFAULT_AGENTS_MD_SIZE_BUDGET,
        override: options.sizeBudget,
      });

      const outputPath = options.outputPath || agentsMdOutputPath(workspacePath);
      const contentsByPath = new Map<string, string>([[outputPath, content]]);

      if (!options.dryRun) {
        await fs.writeFile(outputPath, content, "utf-8");
      }

      return {
        target: "agents.md",
        filesWritten: [outputPath],
        definitionsIncluded: rulesIncluded,
        definitionsSkipped: rulesSkipped,
        content: options.dryRun ? content : undefined,
        contentsByPath: options.dryRun ? contentsByPath : undefined,
        sizeChars: sizeEvaluation.sizeChars,
        sizeBudget: sizeEvaluation.budget,
        sizeBudgetStatus: sizeEvaluation.status,
        topContributors: sizeEvaluation.topContributors,
        ruleContentChars: sizeEvaluation.ruleContentChars,
        perRuleViolations: sizeEvaluation.perRuleViolations,
      };
    },
  };
}

export const agentsMdTarget = makeAgentsMdTarget();

/** Export factory for test injection */
export { makeAgentsMdTarget };
