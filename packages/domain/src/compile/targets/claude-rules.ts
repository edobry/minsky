/**
 * Claude Rules Compile Target (new pipeline — mt#2992, Phase 1 of ADR-016
 * convergence)
 *
 * Ports the legacy `claude-rules` target's content-assembly + stale-file
 * removal logic (`packages/domain/src/rules/compile/targets/claude-rules.ts`,
 * mt#2868) onto the new `compile` pipeline's rule source — see
 * `claude-md.ts`'s module doc for the shared rationale (dormancy, ordering,
 * banner-unchanged) that applies here identically. See the legacy target's
 * module doc for the full design-decision rationale (hard-constraint
 * eligibility, flow-sequence `paths:` frontmatter, glob bracket-escaping,
 * project-scope-only, stale-file removal) — ported unchanged in behavior.
 *
 * **`sharedOutputDirectory: true` (staleness-check divergence from legacy
 * write-time behavior).** `.claude/rules/` legitimately holds BOTH
 * generated files (carrying the banner) and hand-authored ones (without
 * it) — the legacy target's own removal logic already only ever touches
 * banner-carrying files (see `compile()` below). But the new pipeline's
 * generic `--check` staleness detector (`../staleness.ts`) has no concept
 * of "banner-carrying vs hand-authored" — it flags ANY on-disk file whose
 * basename isn't in `listOutputFiles()`'s expected set as an orphan needing
 * removal. Declaring `sharedOutputDirectory: true` skips that generic
 * orphan check (mirroring `cursor-rules-ts.ts`/`claude-agents.ts`, which
 * make the same declaration for the same reason), so `--check` never
 * misclassifies a hand-authored file as stale. The actual (banner-gated)
 * removal behavior is implemented directly in `compile()` below and fires
 * only on a real (non-dry-run) write, exactly matching the legacy target's
 * write-time-only removal.
 */

import { join, basename } from "path";
import realFs from "fs/promises";
import type { Rule } from "../../rules/types";
import type {
  MinskyCompileTarget,
  MinskyCompileResult,
  MinskyTargetOptions,
  MinskyCompileFsDeps,
} from "../types";
import { loadAdaptedRules, type SkipLogFn, type DynamicImportFn } from "./rule-loader";

/**
 * Banner emitted in the BODY of every generated `.claude/rules/*.md` file,
 * immediately after the closing frontmatter `---`. Matches the legacy
 * `rules/compile/targets/claude-rules.ts`'s `CLAUDE_RULES_BANNER` exactly —
 * duplicated as a literal (not imported), the same precedent that module
 * itself already establishes (it is ALSO duplicated as a literal in
 * `crud-operations.ts` and `src/hooks/pre-commit.ts`) — keep all four in
 * sync if this text ever changes.
 */
export const CLAUDE_RULES_BANNER =
  "<!-- Generated by minsky rules compile. Do not edit directly. -->";

/**
 * Eligibility predicate (legacy design decision 1 — hard constraint). A rule
 * may be emitted to `.claude/rules/` ONLY when it has at least one glob AND
 * `alwaysApply` is the literal boolean `false`.
 */
export function isEligibleForClaudeRules(rule: Rule): boolean {
  return Array.isArray(rule.globs) && rule.globs.length > 0 && rule.alwaysApply === false;
}

/**
 * Escape a literal `[` as `\[` so Claude Code's glob matcher treats it as a
 * literal character rather than the start of a character class (legacy
 * design decision 4). Only `[` is escaped; an already-escaped `\[` is
 * preserved as-is (no double-escape).
 */
export function sanitizeGlobForClaudeRules(glob: string): string {
  return glob.replace(/(?<!\\)\[/g, "\\[");
}

/** YAML single-quote a string: only `'` needs escaping (as `''`). */
function yamlSingleQuote(value: string): string {
  return `'${value.replace(/'/g, "''")}'`;
}

/** Build the flow-style `paths:` frontmatter line for a rule's globs. */
function buildPathsFrontmatterLine(globs: string[]): string {
  const items = globs.map((g) => yamlSingleQuote(sanitizeGlobForClaudeRules(g)));
  return `paths: [${items.join(", ")}]`;
}

/**
 * Serialize an eligible Rule to `.claude/rules/*.md` format: `paths:`
 * frontmatter, generation banner in the body, then the rule content.
 */
export function serializeRuleToClaudeRule(rule: Rule): string {
  const globs = rule.globs ?? [];
  const pathsLine = buildPathsFrontmatterLine(globs);
  return `---\n${pathsLine}\n---\n${CLAUDE_RULES_BANNER}\n\n${rule.content}`;
}

export interface ClaudeRulesFileList {
  files: Array<{ path: string; content: string }>;
  rulesIncluded: string[];
  rulesSkipped: string[];
}

/**
 * Build the list of files that would be written for the claude-rules target.
 * Only eligible rules (non-empty globs, alwaysApply: false) produce a file;
 * every other rule is reported in `rulesSkipped` for transparency.
 */
export function buildClaudeRulesContent(rules: Rule[], outputDir: string): ClaudeRulesFileList {
  const files: Array<{ path: string; content: string }> = [];
  const rulesIncluded: string[] = [];
  const rulesSkipped: string[] = [];

  for (const rule of rules) {
    if (!isEligibleForClaudeRules(rule)) {
      rulesSkipped.push(rule.id);
      continue;
    }
    const filePath = join(outputDir, `${rule.id}.md`);
    const content = serializeRuleToClaudeRule(rule);
    files.push({ path: filePath, content });
    rulesIncluded.push(rule.id);
  }

  return { files, rulesIncluded, rulesSkipped };
}

function claudeRulesOutputDir(workspacePath: string): string {
  return join(workspacePath, ".claude", "rules");
}

/** Build the claude-rules target, injecting fs/dynamicImport/skip-log for tests. */
function makeClaudeRulesTarget(
  dynamicImport?: DynamicImportFn,
  onSkip?: SkipLogFn
): MinskyCompileTarget {
  return {
    id: "claude-rules",
    displayName: "Claude Rules (.claude/rules/)",
    sharedOutputDirectory: true,

    defaultOutputPath(workspacePath: string): string {
      return claudeRulesOutputDir(workspacePath);
    },

    async listOutputFiles(
      options: MinskyTargetOptions,
      workspacePath: string,
      fsDeps?: MinskyCompileFsDeps
    ): Promise<string[]> {
      const fs = fsDeps ?? (realFs as MinskyCompileFsDeps);
      const rules = await loadAdaptedRules(workspacePath, fs, onSkip, dynamicImport);
      const outputDir = options.outputPath || claudeRulesOutputDir(workspacePath);
      const { files } = buildClaudeRulesContent(rules, outputDir);
      return files.map((f) => f.path);
    },

    async compile(
      options: MinskyTargetOptions,
      workspacePath: string,
      fsDeps?: MinskyCompileFsDeps
    ): Promise<MinskyCompileResult> {
      const fs = fsDeps ?? (realFs as MinskyCompileFsDeps);
      const rules = await loadAdaptedRules(workspacePath, fs, onSkip, dynamicImport);
      const outputDir = options.outputPath || claudeRulesOutputDir(workspacePath);

      const { files, rulesIncluded, rulesSkipped } = buildClaudeRulesContent(rules, outputDir);

      const filesWritten: string[] = [];
      const contentsByPath = new Map<string, string>();

      if (options.dryRun) {
        for (const { path: filePath, content } of files) {
          contentsByPath.set(filePath, content);
          filesWritten.push(filePath);
        }
      } else {
        await fs.mkdir(outputDir, { recursive: true });
        const expectedBasenames = new Set(files.map((f) => basename(f.path)));
        for (const { path: filePath, content } of files) {
          await fs.writeFile(filePath, content, "utf-8");
          filesWritten.push(filePath);
        }

        // Stale-file removal (see module doc). Only files that themselves
        // carry the generation banner are candidates — a `.claude/rules/*.md`
        // file without it is treated as user-authored and never touched.
        try {
          const entries = await fs.readdir(outputDir);
          for (const entry of entries) {
            if (!entry.endsWith(".md")) continue;
            if (expectedBasenames.has(entry)) continue;
            const entryPath = join(outputDir, entry);
            let existing: string;
            try {
              existing = String(await fs.readFile(entryPath, "utf-8"));
            } catch {
              continue; // Unreadable — skip rather than risk deleting blind.
            }
            if (existing.includes(CLAUDE_RULES_BANNER)) {
              await fs.unlink?.(entryPath);
            }
          }
        } catch {
          // outputDir didn't exist before mkdir (fresh compile) — nothing stale.
        }
      }

      return {
        target: "claude-rules",
        filesWritten,
        definitionsIncluded: rulesIncluded,
        definitionsSkipped: rulesSkipped,
        content: options.dryRun
          ? files.map(({ path: p, content: c }) => `// ${p}\n${c}`).join("\n\n")
          : undefined,
        contentsByPath: options.dryRun ? contentsByPath : undefined,
      };
    },
  };
}

export const claudeRulesTarget = makeClaudeRulesTarget();

/** Export factory for test injection */
export { makeClaudeRulesTarget };
