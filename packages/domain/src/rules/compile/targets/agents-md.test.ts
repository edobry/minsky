import { describe, it, expect } from "bun:test";
import { agentsMdTarget, buildContent, DEFAULT_AGENTS_MD_SIZE_BUDGET } from "./agents-md";
import {
  ALWAYS_APPLY_CONTENT,
  MANUAL_RULE_CONTENT,
  GLOB_RULE_CONTENT,
  PROJECT_INSTRUCTIONS_HEADER,
} from "./test-fixtures";
import { makeRule } from "../test-utils";

describe("agents-md target: listOutputFiles()", () => {
  it("returns single path at <workspace>/AGENTS.md", () => {
    const paths = agentsMdTarget.listOutputFiles([], {}, "/workspace");
    expect(paths).toHaveLength(1);
    expect(paths[0]).toBe("/workspace/AGENTS.md");
  });

  it("respects custom outputPath option", () => {
    const paths = agentsMdTarget.listOutputFiles(
      [],
      { outputPath: "/custom/AGENTS.md" },
      "/workspace"
    );
    expect(paths).toHaveLength(1);
    expect(paths[0]).toBe("/custom/AGENTS.md");
  });
});

describe("agents-md target: buildContent()", () => {
  describe("basic compilation", () => {
    it("output starts with the generation comment", () => {
      const { content } = buildContent([], {});
      expect(content).toContain(
        "<!-- Generated by minsky rules compile. Do not edit directly. -->"
      );
      expect(content.startsWith("<!-- Generated by minsky rules compile.")).toBe(true);
    });

    it("output has # Project Instructions header", () => {
      const { content } = buildContent([], {});
      expect(content).toContain(PROJECT_INSTRUCTIONS_HEADER);
    });

    it("section headers appear in order matching the mapping", () => {
      const sectionMapping = {
        "Section A": ["rule-a"],
        "Section B": ["rule-b"],
        "Section C": ["rule-c"],
      };
      const rules = [
        makeRule("rule-a", "Content of A"),
        makeRule("rule-b", "Content of B"),
        makeRule("rule-c", "Content of C"),
      ];

      const { content } = buildContent(rules, sectionMapping);

      const posA = content.indexOf("## Section A");
      const posB = content.indexOf("## Section B");
      const posC = content.indexOf("## Section C");

      expect(posA).toBeGreaterThan(-1);
      expect(posB).toBeGreaterThan(-1);
      expect(posC).toBeGreaterThan(-1);
      expect(posA).toBeLessThan(posB);
      expect(posB).toBeLessThan(posC);
    });

    it("rule content appears under its correct section", () => {
      const sectionMapping = {
        "Build & Test": ["bun-rule"],
        Architecture: ["arch-rule"],
      };
      const rules = [
        makeRule("bun-rule", "Use bun, not node"),
        makeRule("arch-rule", "Clean architecture"),
      ];

      const { content } = buildContent(rules, sectionMapping);

      const buildIdx = content.indexOf("## Build & Test");
      const archIdx = content.indexOf("## Architecture");
      const bunContentIdx = content.indexOf("Use bun, not node");
      const archContentIdx = content.indexOf("Clean architecture");

      expect(buildIdx).toBeGreaterThan(-1);
      expect(archIdx).toBeGreaterThan(-1);
      expect(bunContentIdx).toBeGreaterThan(buildIdx);
      expect(bunContentIdx).toBeLessThan(archIdx);
      expect(archContentIdx).toBeGreaterThan(archIdx);
    });
  });

  describe("always-apply collection into General section", () => {
    it("alwaysApply rules not in section mapping go into ## General", () => {
      const sectionMapping = {
        "Build & Test": ["mapped-rule"],
      };
      const rules = [
        makeRule("mapped-rule", "Mapped rule content"),
        makeRule("unmapped-always", ALWAYS_APPLY_CONTENT, { alwaysApply: true }),
      ];

      const { content } = buildContent(rules, sectionMapping);

      expect(content).toContain("## General");
      expect(content).toContain(ALWAYS_APPLY_CONTENT);

      const generalIdx = content.indexOf("## General");
      const contentIdx = content.indexOf(ALWAYS_APPLY_CONTENT);
      expect(contentIdx).toBeGreaterThan(generalIdx);
    });

    it("alwaysApply rules that ARE in section mapping do NOT appear in General", () => {
      const sectionMapping = {
        "Build & Test": ["always-mapped"],
      };
      const rules = [
        makeRule("always-mapped", "Mapped always-apply content", { alwaysApply: true }),
      ];

      const { content } = buildContent(rules, sectionMapping);

      expect(content).not.toContain("## General");
      expect(content).toContain("## Build & Test");
      expect(content).toContain("Mapped always-apply content");
    });

    it("non-alwaysApply rules not in section mapping are skipped", () => {
      const sectionMapping = {
        "Build & Test": ["mapped-rule"],
      };
      const rules = [
        makeRule("mapped-rule", "Mapped rule content"),
        makeRule("unmapped-manual", MANUAL_RULE_CONTENT),
        makeRule("unmapped-glob", GLOB_RULE_CONTENT, { globs: ["**/*.ts"] }),
      ];

      const { content } = buildContent(rules, sectionMapping);

      expect(content).not.toContain("## General");
      expect(content).not.toContain(MANUAL_RULE_CONTENT);
      expect(content).not.toContain(GLOB_RULE_CONTENT);
    });
  });

  describe("missing rules handled gracefully", () => {
    it("silently skips rule IDs in mapping that do not exist", () => {
      const sectionMapping = {
        "Build & Test": ["existing-rule", "nonexistent-rule"],
      };
      const rules = [makeRule("existing-rule", "Existing content")];

      const { content } = buildContent(rules, sectionMapping);
      expect(content).toContain("Existing content");
    });

    it("section is omitted entirely if all its rules are missing", () => {
      const sectionMapping = {
        "Build & Test": ["nonexistent-1", "nonexistent-2"],
        Architecture: ["real-rule"],
      };
      const rules = [makeRule("real-rule", "Real content")];

      const { content } = buildContent(rules, sectionMapping);

      expect(content).not.toContain("## Build & Test");
      expect(content).toContain("## Architecture");
    });
  });

  describe("rulesIncluded and rulesSkipped tracking", () => {
    it("included section rules appear in rulesIncluded", () => {
      const sectionMapping = {
        "Build & Test": ["rule-a", "rule-b"],
      };
      const rules = [makeRule("rule-a", "Content A"), makeRule("rule-b", "Content B")];

      const { rulesIncluded } = buildContent(rules, sectionMapping);

      expect(rulesIncluded).toContain("rule-a");
      expect(rulesIncluded).toContain("rule-b");
    });

    it("rules not included are listed in rulesSkipped", () => {
      const sectionMapping = {
        "Build & Test": ["included-rule"],
      };
      const rules = [makeRule("included-rule", "Included"), makeRule("skipped-rule", "Skipped")];

      const { rulesSkipped } = buildContent(rules, sectionMapping);

      expect(rulesSkipped).toContain("skipped-rule");
      expect(rulesSkipped).not.toContain("included-rule");
    });

    it("alwaysApply rules in General are NOT in rulesSkipped", () => {
      const sectionMapping: Record<string, string[]> = {};
      const rules = [
        makeRule("always-rule", "Always content", { alwaysApply: true }),
        makeRule("manual-rule", "Manual content"),
      ];

      const { rulesSkipped } = buildContent(rules, sectionMapping);

      expect(rulesSkipped).not.toContain("always-rule");
      expect(rulesSkipped).toContain("manual-rule");
    });
  });
});

// Note: `agentsMdTarget.compile()` writes via a module-level `fs/promises`
// import (no fs-injection seam), so its size-budget wiring is NOT exercised
// here with real filesystem I/O (forbidden by `custom/no-real-fs-in-tests`).
// It shares the exact same `evaluateSizeBudget()` call shape as the
// dry-run path (`compileDryRun` in compile-service.ts), which IS exercised
// end-to-end — with an injected mock fs — by `../check.test.ts`'s
// "compileRules --check size budget" suite. `./size-budget.test.ts` covers
// the evaluation logic itself in isolation.
describe("agents-md target: DEFAULT_AGENTS_MD_SIZE_BUDGET (mt#2802)", () => {
  it("current-corpus defaults are grounded: warn=160000, fail=200000", () => {
    expect(DEFAULT_AGENTS_MD_SIZE_BUDGET).toEqual({ warnChars: 160_000, failChars: 200_000 });
  });

  it("fail sits strictly above warn", () => {
    expect(DEFAULT_AGENTS_MD_SIZE_BUDGET.failChars).toBeGreaterThan(
      DEFAULT_AGENTS_MD_SIZE_BUDGET.warnChars
    );
  });
});
