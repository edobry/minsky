/**
 * Claude Rules Compile Target (mt#2868)
 *
 * Compiles ELIGIBLE rules into individual `.claude/rules/<id>.md` files with
 * `paths:` frontmatter, giving Claude Code a native, lazy-loading delivery
 * channel for the non-`alwaysApply` rule corpus (sibling of `cursor-rules`,
 * which targets Cursor's `.cursor/rules/*.mdc`).
 *
 * Design decisions (grounded in vendor docs, verified 2026-07-15 — see the
 * mt#2868 task spec "Design decisions" section for the full research trail):
 *
 * 1. HARD CONSTRAINT — emit ONLY rules with non-empty `globs` AND
 *    `alwaysApply === false`. A `.claude/rules/*.md` file WITHOUT `paths:`
 *    frontmatter loads unconditionally at session start, same priority as
 *    CLAUDE.md — emitting a glob-less rule here would silently re-bloat the
 *    always-loaded budget the mt#1876/mt#1877/mt#2802 workstream just fixed.
 *    `alwaysApply` must be the LITERAL boolean `false` (not merely falsy /
 *    undefined) — a rule that never declared a classification either way
 *    stays excluded rather than being inferred into eligibility.
 * 2. Frontmatter is exactly `paths:` as a YAML list of glob strings. No other
 *    frontmatter fields are documented for `.claude/rules/*.md`. Emitted in
 *    YAML FLOW-SEQUENCE style (`paths: ['a', 'b']`) rather than block style —
 *    this is still a valid YAML list (satisfies the vendor-doc requirement
 *    and the `^paths:` acceptance grep) while keeping the frontmatter block
 *    to exactly 3 lines regardless of glob count, so the generation banner
 *    below always lands on line 4 — safely inside the 5-line scan window
 *    `.claude/hooks/check-generated-file-edit.ts` uses to detect generated
 *    files (a block-style multi-glob list would push the banner past line 5
 *    for any rule with 2+ globs, silently defeating that guard).
 * 3. The generation banner is an HTML comment in the BODY, immediately after
 *    the closing `---` — never as a frontmatter field. Reuses the same
 *    literal AGENTS.md/CLAUDE.md already emit on their first line, so a
 *    single detection string works across all three legacy targets.
 * 4. Glob sanitization: a literal `[` is escaped to `\[` before embedding —
 *    an unescaped bracket makes the pattern silently match nothing (Claude
 *    Code v2.1.207 fail-closed behavior per vendor-doc research). The escape
 *    happens on the glob's own characters; YAML single-quoting the resulting
 *    string then preserves the backslash literally (single-quoted YAML
 *    scalars have no escape processing other than `''` for a literal quote).
 * 5. Project scope only — never emit to `~/.claude/rules/` (community bug
 *    reports `paths:` is silently ignored at user-global scope).
 *
 * Stale-file removal: unlike `cursor-rules` (which never deletes orphaned
 * `.mdc` files — see that target's module doc), this target actively removes
 * previously-generated `.claude/rules/<id>.md` files whose source rule lost
 * eligibility (globs cleared, `alwaysApply` flipped to `true`, or the rule
 * was disabled via selection config so it's absent from the `rules` array
 * entirely). This divergence from `cursor-rules` is deliberate, not an
 * oversight: design decision 1 above treats a stray glob-less `.claude/rules/`
 * file as an always-loaded-budget hazard, so leaving stale files behind (as
 * `cursor-rules` does) would reintroduce exactly the hazard this target
 * exists to avoid. Only files that themselves carry the generation banner
 * are ever removed — a `.claude/rules/*.md` file without the banner is
 * treated as user-authored and left untouched.
 */

import { join, basename } from "path";
import * as fs from "fs/promises";
import type { Rule } from "../../types";
import type { CompileTarget, CompileResult, TargetOptions } from "../types";

/**
 * Banner emitted in the BODY of every generated `.claude/rules/*.md` file,
 * immediately after the closing frontmatter `---` (design decision 3). This
 * is the same literal AGENTS.md/CLAUDE.md emit as their first line — reusing
 * it (rather than the YAML-comment `GENERATED_BANNER` cursor-rules uses)
 * keeps a single detection string usable across all HTML-comment-banner
 * targets. Duplicated as a literal (not imported) in
 * `packages/domain/src/rules/operations/crud-operations.ts` and
 * `src/hooks/pre-commit.ts` for the same reason `LEGACY_COMPILE_HEADER` is
 * duplicated there — keep all three in sync if this text ever changes.
 */
export const CLAUDE_RULES_BANNER =
  "<!-- Generated by minsky rules compile. Do not edit directly. -->";

/**
 * Eligibility predicate (design decision 1 — hard constraint). A rule may be
 * emitted to `.claude/rules/` ONLY when it has at least one glob AND
 * `alwaysApply` is the literal boolean `false`.
 */
export function isEligibleForClaudeRules(rule: Rule): boolean {
  return Array.isArray(rule.globs) && rule.globs.length > 0 && rule.alwaysApply === false;
}

/**
 * Escape a literal `[` as `\[` so Claude Code's glob matcher treats it as a
 * literal character rather than the start of a character class (design
 * decision 4). Only `[` is escaped — `]` is left alone per the verified
 * vendor-doc behavior (an unescaped `[` is what silently fails to match; a
 * lone `]` outside a class has no special meaning to escape).
 */
export function sanitizeGlobForClaudeRules(glob: string): string {
  return glob.replace(/\[/g, "\\[");
}

/** YAML single-quote a string: only `'` needs escaping (as `''`); backslash
 * is preserved literally in single-quoted YAML scalars, which is exactly
 * what the sanitized `\[` sequence needs to survive round-trip. */
function yamlSingleQuote(value: string): string {
  return `'${value.replace(/'/g, "''")}'`;
}

/** Build the flow-style `paths:` frontmatter line for a rule's globs. */
function buildPathsFrontmatterLine(globs: string[]): string {
  const items = globs.map((g) => yamlSingleQuote(sanitizeGlobForClaudeRules(g)));
  return `paths: [${items.join(", ")}]`;
}

/**
 * Serialize an eligible Rule to `.claude/rules/*.md` format: `paths:`
 * frontmatter, generation banner in the body, then the rule content.
 */
export function serializeRuleToClaudeRule(rule: Rule): string {
  const globs = rule.globs ?? [];
  const pathsLine = buildPathsFrontmatterLine(globs);
  return `---\n${pathsLine}\n---\n${CLAUDE_RULES_BANNER}\n\n${rule.content}`;
}

export interface ClaudeRulesFileList {
  files: Array<{ path: string; content: string }>;
  rulesIncluded: string[];
  rulesSkipped: string[];
}

/**
 * Build the list of files that would be written for the claude-rules target.
 * Only eligible rules (non-empty globs, alwaysApply: false) produce a file;
 * every other rule is reported in `rulesSkipped` for transparency.
 */
export function buildClaudeRulesContent(rules: Rule[], outputDir: string): ClaudeRulesFileList {
  const files: Array<{ path: string; content: string }> = [];
  const rulesIncluded: string[] = [];
  const rulesSkipped: string[] = [];

  for (const rule of rules) {
    if (!isEligibleForClaudeRules(rule)) {
      rulesSkipped.push(rule.id);
      continue;
    }
    const filePath = join(outputDir, `${rule.id}.md`);
    const content = serializeRuleToClaudeRule(rule);
    files.push({ path: filePath, content });
    rulesIncluded.push(rule.id);
  }

  return { files, rulesIncluded, rulesSkipped };
}

/**
 * Claude Rules compile target implementation. Writes each ELIGIBLE rule as a
 * separate `.md` file to `.claude/rules/`, and removes previously-generated
 * files whose source rule is no longer eligible (see module doc for why this
 * target diverges from `cursor-rules`' no-removal behavior).
 */
export const claudeRulesTarget: CompileTarget = {
  id: "claude-rules",
  displayName: "Claude Rules (.claude/rules/)",

  defaultOutputPath(workspacePath: string): string {
    return join(workspacePath, ".claude", "rules");
  },

  listOutputFiles(rules: Rule[], options: TargetOptions, workspacePath: string): string[] {
    const outputDir = options.outputPath || this.defaultOutputPath(workspacePath);
    const { files } = buildClaudeRulesContent(rules, outputDir);
    return files.map((f) => f.path);
  },

  async compile(
    rules: Rule[],
    options: TargetOptions,
    workspacePath: string
  ): Promise<CompileResult> {
    const outputDir = options.outputPath || this.defaultOutputPath(workspacePath);

    await fs.mkdir(outputDir, { recursive: true });

    const { files, rulesIncluded, rulesSkipped } = buildClaudeRulesContent(rules, outputDir);

    const filesWritten: string[] = [];
    const expectedBasenames = new Set(files.map((f) => basename(f.path)));
    for (const { path: filePath, content } of files) {
      await fs.writeFile(filePath, content, "utf-8");
      filesWritten.push(filePath);
    }

    // Stale-file removal (see module doc). Only files that themselves carry
    // the generation banner are candidates — a `.claude/rules/*.md` file
    // without it is treated as user-authored and never touched.
    try {
      const entries = await fs.readdir(outputDir);
      for (const entry of entries) {
        if (!entry.endsWith(".md")) continue;
        if (expectedBasenames.has(entry)) continue;
        const entryPath = join(outputDir, entry);
        let existing: string;
        try {
          existing = String(await fs.readFile(entryPath, "utf-8"));
        } catch {
          continue; // Unreadable — skip rather than risk deleting blind.
        }
        if (existing.includes(CLAUDE_RULES_BANNER)) {
          await fs.unlink(entryPath);
        }
      }
    } catch {
      // outputDir didn't exist before mkdir (fresh compile) — nothing stale.
    }

    return {
      target: this.id,
      filesWritten,
      rulesIncluded,
      rulesSkipped,
    };
  },
};
