/**
 * Generated-file banner shared constants (mt#1798).
 *
 * Single source of truth for:
 * - The banner string emitted by the `rules compile` pipeline into compiled
 *   outputs (CLAUDE.md, AGENTS.md, .cursor/rules/*.mdc).
 * - The regex patterns that `.claude/hooks/check-generated-file-edit.ts`
 *   uses to detect compiled outputs and block direct edits.
 *
 * Having both in one module eliminates drift between the emission side
 * (compile writers) and the detection side (the PreToolUse hook).
 *
 * The hook imports `GENERATION_BANNER_PATTERNS` from here; writers import
 * `GENERATED_BANNER`. Tests import both and assert that the banner string
 * matches at least one pattern (regression guard).
 */

/**
 * Banner text emitted by the rules-compile pipeline into compiled outputs.
 *
 * For `.cursor/rules/*.mdc` outputs the banner is emitted as a YAML
 * line-comment inside the frontmatter block (on line 2; line 1 remains the
 * `---` frontmatter opener required by Cursor's strict `.mdc` parser).
 * For CLAUDE.md / AGENTS.md the writers use an HTML-comment form on line 1.
 * Both forms match the `GENERATION_BANNER_PATTERNS` below.
 */
export const GENERATED_BANNER = "# Generated by minsky rules compile. Do not edit directly.";

/**
 * Banner text emitted by the new `compile` pipeline (definition-derived
 * outputs: `.claude/skills/*\/SKILL.md`, etc.) — distinct from the legacy
 * `rules compile` `GENERATED_BANNER` above so provenance is distinguishable,
 * while both still match the `GENERATION_BANNER_PATTERNS` below. For SKILL.md
 * (and any frontmatter-bearing output) this is emitted as a YAML line-comment
 * on line 2 (line 1 remains the `---` frontmatter opener), matching the
 * `.cursor/rules/*.mdc` convention. It matches both the `# Generated by`
 * hash-comment pattern and the `do not edit directly` verbal pattern.
 */
export const COMPILE_GENERATED_BANNER = "# Generated by minsky compile. Do not edit directly.";

/**
 * Banner-detection patterns. Each entry has a regex matching one common
 * banner form used by code generators. The `check-generated-file-edit`
 * PreToolUse hook checks against the first 5 lines of any file targeted by
 * a write/edit tool call and blocks the call if any pattern matches.
 *
 * If you add a new compile target that emits the banner in a new comment
 * form, add a new entry here so the hook recognises it.
 */
export const GENERATION_BANNER_PATTERNS: ReadonlyArray<{ re: RegExp; name: string }> = [
  {
    re: /<!--\s*Generated by\b/i,
    name: "HTML comment: Generated by",
  },
  {
    re: /<!--\s*Do not edit/i,
    name: "HTML comment: Do not edit",
  },
  {
    re: /^\s*\/\/\s*(AUTOGENERATED|Generated by\b)/im,
    name: "line comment: AUTOGENERATED / Generated by",
  },
  {
    re: /\/\*\s*Generated by\b/i,
    name: "block comment: Generated by",
  },
  {
    re: /^\s*#\s*Generated by\b/im,
    name: "hash comment: Generated by",
  },
  {
    re: /do[-\s]not[-\s]edit\s+(directly|this)/i,
    name: "verbal form: do not edit directly/this",
  },
];
