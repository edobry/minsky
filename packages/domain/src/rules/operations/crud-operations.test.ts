/**
 * Unit tests for the mt#2803 bare-invocation target-probing fix.
 *
 * Scope: the probe-based default-target resolution added by mt#2803
 * (`probeLegacyCompileTargets` and `compileRules`'s dispatch branching), NOT
 * the individual target compilers' output content (covered by their own
 * fixtures / mt#2802's size-budget tests). The legacy compile targets
 * (agents-md.ts, claude-md.ts) write via real `fs/promises` regardless of
 * any injected fs deps, so these tests use real scratch directories under
 * the OS temp dir rather than an in-memory fake.
 */
/* eslint-disable custom/no-real-fs-in-tests -- the legacy compile targets
 * (agents-md.ts, claude-md.ts) hardcode `import * as fs from "fs/promises"`
 * internally with no injectable fs seam for the WRITE path (unlike the new
 * compile-pipeline targets, which do accept an injectable MinskyCompileFsDeps
 * — see compile.test.ts, which uses a fully in-memory fake). Testing the
 * bare-invocation multi-target regeneration end-to-end therefore requires a
 * real scratch directory; mkdtemp() under the OS tmpdir + afterEach cleanup
 * avoids the race-condition/parallel-test concerns the rule guards against. */

import { describe, it, expect, afterEach } from "bun:test";
import { mkdtemp, mkdir, writeFile, readFile, rm } from "fs/promises";
import { tmpdir } from "os";
import { join } from "path";
import realFs from "fs/promises";
import { compileRules, probeLegacyCompileTargets } from "./crud-operations";
import type { RuleServiceFs } from "../../rules";

const GENERATED_BANNER = "<!-- Generated by minsky rules compile. Do not edit directly. -->";

async function makeScratchDir(): Promise<string> {
  return mkdtemp(join(tmpdir(), "mt2803-crud-ops-"));
}

let scratchDirs: string[] = [];

afterEach(async () => {
  await Promise.all(scratchDirs.map((dir) => rm(dir, { recursive: true, force: true })));
  scratchDirs = [];
});

async function withScratchDir(): Promise<string> {
  const dir = await makeScratchDir();
  scratchDirs.push(dir);
  return dir;
}

describe("probeLegacyCompileTargets (mt#2803)", () => {
  it("returns an empty array for a fresh repo with no generated outputs", async () => {
    const workspacePath = await withScratchDir();
    const targets = await probeLegacyCompileTargets(workspacePath, realFs as RuleServiceFs);
    expect(targets).toEqual([]);
  });

  it("does not detect AGENTS.md when it lacks the generated banner (hand-authored)", async () => {
    const workspacePath = await withScratchDir();
    await writeFile(join(workspacePath, "AGENTS.md"), "# Hand-authored instructions\n", "utf-8");
    const targets = await probeLegacyCompileTargets(workspacePath, realFs as RuleServiceFs);
    expect(targets).toEqual([]);
  });

  it("detects agents.md when AGENTS.md carries the generated banner", async () => {
    const workspacePath = await withScratchDir();
    await writeFile(join(workspacePath, "AGENTS.md"), `${GENERATED_BANNER}\ncontent\n`, "utf-8");
    const targets = await probeLegacyCompileTargets(workspacePath, realFs as RuleServiceFs);
    expect(targets).toEqual(["agents.md"]);
  });

  it("detects claude.md when CLAUDE.md carries the generated banner", async () => {
    const workspacePath = await withScratchDir();
    await writeFile(join(workspacePath, "CLAUDE.md"), `${GENERATED_BANNER}\ncontent\n`, "utf-8");
    const targets = await probeLegacyCompileTargets(workspacePath, realFs as RuleServiceFs);
    expect(targets).toEqual(["claude.md"]);
  });

  it("detects cursor-rules when .cursor/rules/ has at least one .mdc file", async () => {
    const workspacePath = await withScratchDir();
    await mkdir(join(workspacePath, ".cursor", "rules"), { recursive: true });
    await writeFile(
      join(workspacePath, ".cursor", "rules", "some-rule.mdc"),
      "---\nname: Some Rule\n---\ncontent\n",
      "utf-8"
    );
    const targets = await probeLegacyCompileTargets(workspacePath, realFs as RuleServiceFs);
    expect(targets).toEqual(["cursor-rules"]);
  });

  it("does not detect cursor-rules when .cursor/rules/ exists but has no .mdc files", async () => {
    const workspacePath = await withScratchDir();
    await mkdir(join(workspacePath, ".cursor", "rules"), { recursive: true });
    await writeFile(join(workspacePath, ".cursor", "rules", "README.txt"), "not a rule", "utf-8");
    const targets = await probeLegacyCompileTargets(workspacePath, realFs as RuleServiceFs);
    expect(targets).toEqual([]);
  });

  it("detects all three targets together, in agents.md/claude.md/cursor-rules order", async () => {
    const workspacePath = await withScratchDir();
    await writeFile(join(workspacePath, "AGENTS.md"), `${GENERATED_BANNER}\ncontent\n`, "utf-8");
    await writeFile(join(workspacePath, "CLAUDE.md"), `${GENERATED_BANNER}\ncontent\n`, "utf-8");
    await mkdir(join(workspacePath, ".cursor", "rules"), { recursive: true });
    await writeFile(
      join(workspacePath, ".cursor", "rules", "some-rule.mdc"),
      "---\nname: Some Rule\n---\ncontent\n",
      "utf-8"
    );
    const targets = await probeLegacyCompileTargets(workspacePath, realFs as RuleServiceFs);
    expect(targets).toEqual(["agents.md", "claude.md", "cursor-rules"]);
  });
});

describe("compileRules — bare-invocation default-target resolution (mt#2803)", () => {
  it("falls back to the single agents.md default on a fresh repo (no existing outputs)", async () => {
    const workspacePath = await withScratchDir();

    const result = await compileRules({ workspacePath });

    expect(result.success).toBe(true);
    expect(result.target).toBe("agents.md");
    expect(result.targets).toBeUndefined();

    const written = await readFile(join(workspacePath, "AGENTS.md"), "utf-8");
    expect(written.startsWith(GENERATED_BANNER)).toBe(true);
  });

  it("compiles every existing target in one bare invocation (partial regen made whole)", async () => {
    const workspacePath = await withScratchDir();
    // Seed AGENTS.md and CLAUDE.md with the generated banner but stale body
    // content, simulating the pre-mt#2803 trap: a bare invocation used to
    // touch only agents.md, leaving CLAUDE.md silently stale.
    const staleBody = `${GENERATED_BANNER}\nSTALE PLACEHOLDER BODY\n`;
    await writeFile(join(workspacePath, "AGENTS.md"), staleBody, "utf-8");
    await writeFile(join(workspacePath, "CLAUDE.md"), staleBody, "utf-8");

    const result = await compileRules({ workspacePath });

    expect(result.success).toBe(true);
    expect(result.targets).toBeDefined();
    expect(result.targets?.map((t) => t.target)).toEqual(["agents.md", "claude.md"]);
    expect(result.targets?.every((t) => t.success)).toBe(true);

    const agentsContent = await readFile(join(workspacePath, "AGENTS.md"), "utf-8");
    const claudeContent = await readFile(join(workspacePath, "CLAUDE.md"), "utf-8");
    expect(agentsContent.startsWith(GENERATED_BANNER)).toBe(true);
    expect(claudeContent.startsWith(GENERATED_BANNER)).toBe(true);
    // Both files were actually rewritten — the stale placeholder body is gone.
    expect(agentsContent).not.toContain("STALE PLACEHOLDER BODY");
    expect(claudeContent).not.toContain("STALE PLACEHOLDER BODY");
  });

  it("explicit --target compiles exactly one target, ignoring other existing outputs", async () => {
    const workspacePath = await withScratchDir();
    const staleBody = `${GENERATED_BANNER}\nSTALE PLACEHOLDER BODY\n`;
    await writeFile(join(workspacePath, "AGENTS.md"), staleBody, "utf-8");
    await writeFile(join(workspacePath, "CLAUDE.md"), staleBody, "utf-8");

    const result = await compileRules({ workspacePath, target: "agents.md" });

    expect(result.success).toBe(true);
    expect(result.target).toBe("agents.md");
    expect(result.targets).toBeUndefined();

    // CLAUDE.md was left untouched by the explicit-target invocation.
    const claudeContent = await readFile(join(workspacePath, "CLAUDE.md"), "utf-8");
    expect(claudeContent).toBe(staleBody);
  });

  it("--check aggregates staleness across all probed targets rather than stopping at the first", async () => {
    const workspacePath = await withScratchDir();
    const staleBody = `${GENERATED_BANNER}\nSTALE PLACEHOLDER BODY\n`;
    await writeFile(join(workspacePath, "AGENTS.md"), staleBody, "utf-8");
    await writeFile(join(workspacePath, "CLAUDE.md"), staleBody, "utf-8");

    const result = await compileRules({ workspacePath, check: true });

    expect(result.check).toBe(true);
    expect(result.stale).toBe(true);
    expect(result.targets).toBeDefined();
    expect(result.targets?.length).toBe(2);
    // Both targets are independently evaluated and reported stale — the
    // second target's staleness is not masked by stopping at the first.
    const staleTargetIds = result.targets?.filter((t) => t.stale).map((t) => t.target);
    expect(staleTargetIds).toEqual(["agents.md", "claude.md"]);

    // Neither file was rewritten by --check (no side effects).
    const agentsContent = await readFile(join(workspacePath, "AGENTS.md"), "utf-8");
    const claudeContent = await readFile(join(workspacePath, "CLAUDE.md"), "utf-8");
    expect(agentsContent).toBe(staleBody);
    expect(claudeContent).toBe(staleBody);
  });
});
