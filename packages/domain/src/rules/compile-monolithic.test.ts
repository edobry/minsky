import { describe, it, expect } from "bun:test";
import { compileMonolithic } from "./compile-monolithic";
import type { CompileOptions } from "./compile-monolithic";
import type { Rule } from "./types";
import { RuleService } from "../rules";
import { ALWAYS_APPLY_CONTENT, PROJECT_INSTRUCTIONS_HEADER } from "./compile/targets/test-fixtures";

// Helper to create a mock RuleService that returns a fixed list of rules
function createMockRuleService(rules: Rule[]): RuleService {
  return {
    listRules: async () => rules,
  } as unknown as RuleService;
}

// Helper to create a minimal Rule object
function makeRule(id: string, content: string, opts: Partial<Rule> = {}): Rule {
  return {
    id,
    content,
    format: "cursor",
    path: `/fake/path/${id}.mdc`,
    alwaysApply: false,
    ...opts,
  };
}

const WORKSPACE_PATH = "/fake/workspace";

const BASE_OPTIONS: CompileOptions = {
  target: { type: "agents.md" },
  workspacePath: WORKSPACE_PATH,
};

describe("compileMonolithic()", () => {
  describe("basic compilation", () => {
    it("output starts with the generation comment", async () => {
      const ruleService = createMockRuleService([]);
      const result = await compileMonolithic(ruleService, BASE_OPTIONS);
      expect(result.content).toContain(
        "<!-- Generated by minsky rules compile. Do not edit directly. -->"
      );
      expect(result.content.startsWith("<!-- Generated by minsky rules compile.")).toBe(true);
    });

    it("output has # Project Instructions header", async () => {
      const ruleService = createMockRuleService([]);
      const result = await compileMonolithic(ruleService, BASE_OPTIONS);
      expect(result.content).toContain(PROJECT_INSTRUCTIONS_HEADER);
    });

    it("section headers appear in order matching the mapping", async () => {
      const sectionMapping = {
        "Section A": ["rule-a"],
        "Section B": ["rule-b"],
        "Section C": ["rule-c"],
      };
      const rules = [
        makeRule("rule-a", "Content of A"),
        makeRule("rule-b", "Content of B"),
        makeRule("rule-c", "Content of C"),
      ];
      const ruleService = createMockRuleService(rules);
      const result = await compileMonolithic(ruleService, {
        ...BASE_OPTIONS,
        sectionMapping,
      });

      const posA = result.content.indexOf("## Section A");
      const posB = result.content.indexOf("## Section B");
      const posC = result.content.indexOf("## Section C");

      expect(posA).toBeGreaterThan(-1);
      expect(posB).toBeGreaterThan(-1);
      expect(posC).toBeGreaterThan(-1);
      expect(posA).toBeLessThan(posB);
      expect(posB).toBeLessThan(posC);
    });

    it("rule content appears under its correct section", async () => {
      const sectionMapping = {
        "Build & Test": ["bun-rule"],
        Architecture: ["arch-rule"],
      };
      const rules = [
        makeRule("bun-rule", "Use bun, not node"),
        makeRule("arch-rule", "Clean architecture"),
      ];
      const ruleService = createMockRuleService(rules);
      const result = await compileMonolithic(ruleService, {
        ...BASE_OPTIONS,
        sectionMapping,
      });

      const buildIdx = result.content.indexOf("## Build & Test");
      const archIdx = result.content.indexOf("## Architecture");
      const bunContentIdx = result.content.indexOf("Use bun, not node");
      const archContentIdx = result.content.indexOf("Clean architecture");

      expect(buildIdx).toBeGreaterThan(-1);
      expect(archIdx).toBeGreaterThan(-1);
      // "Use bun, not node" should appear after "## Build & Test" header but before "## Architecture"
      expect(bunContentIdx).toBeGreaterThan(buildIdx);
      expect(bunContentIdx).toBeLessThan(archIdx);
      // "Clean architecture" should appear after "## Architecture"
      expect(archContentIdx).toBeGreaterThan(archIdx);
    });

    it("returns correct outputPath based on target type", async () => {
      const ruleService = createMockRuleService([]);
      const result = await compileMonolithic(ruleService, BASE_OPTIONS);
      expect(result.outputPath).toBe(`${WORKSPACE_PATH}/AGENTS.md`);
    });

    it("returns correct outputPath for claude.md target type", async () => {
      const ruleService = createMockRuleService([]);
      const result = await compileMonolithic(ruleService, {
        ...BASE_OPTIONS,
        target: { type: "claude.md" },
      });
      expect(result.outputPath).toBe(`${WORKSPACE_PATH}/CLAUDE.md`);
    });

    it("uses custom outputPath when provided", async () => {
      const ruleService = createMockRuleService([]);
      const result = await compileMonolithic(ruleService, {
        ...BASE_OPTIONS,
        target: { type: "agents.md", outputPath: "/custom/path/OUT.md" },
      });
      expect(result.outputPath).toBe("/custom/path/OUT.md");
    });
  });

  describe("always-apply collection into General section", () => {
    it("alwaysApply rules not in section mapping go into ## General", async () => {
      const sectionMapping = {
        "Build & Test": ["mapped-rule"],
      };
      const rules = [
        makeRule("mapped-rule", "Mapped rule content"),
        makeRule("unmapped-always", ALWAYS_APPLY_CONTENT, { alwaysApply: true }),
      ];
      const ruleService = createMockRuleService(rules);
      const result = await compileMonolithic(ruleService, {
        ...BASE_OPTIONS,
        sectionMapping,
      });

      expect(result.content).toContain("## General");
      expect(result.content).toContain(ALWAYS_APPLY_CONTENT);

      const generalIdx = result.content.indexOf("## General");
      const contentIdx = result.content.indexOf(ALWAYS_APPLY_CONTENT);
      expect(contentIdx).toBeGreaterThan(generalIdx);
    });

    it("alwaysApply rules that ARE in section mapping do NOT appear in General", async () => {
      const sectionMapping = {
        "Build & Test": ["always-mapped"],
      };
      const rules = [
        makeRule("always-mapped", "Mapped always-apply content", { alwaysApply: true }),
      ];
      const ruleService = createMockRuleService(rules);
      const result = await compileMonolithic(ruleService, {
        ...BASE_OPTIONS,
        sectionMapping,
      });

      // Should NOT have a General section since all alwaysApply rules are in mapping
      expect(result.content).not.toContain("## General");
      // Should still appear in Build & Test
      expect(result.content).toContain("## Build & Test");
      expect(result.content).toContain("Mapped always-apply content");
    });

    it("non-alwaysApply rules not in section mapping are skipped, not put in General", async () => {
      const sectionMapping = {
        "Build & Test": ["mapped-rule"],
      };
      const rules = [
        makeRule("mapped-rule", "Mapped rule content"),
        makeRule("unmapped-manual", "Manual rule content"),
        makeRule("unmapped-glob", "Glob rule content", { globs: ["**/*.ts"] }),
      ];
      const ruleService = createMockRuleService(rules);
      const result = await compileMonolithic(ruleService, {
        ...BASE_OPTIONS,
        sectionMapping,
      });

      expect(result.content).not.toContain("## General");
      expect(result.content).not.toContain("Manual rule content");
      expect(result.content).not.toContain("Glob rule content");
    });

    it("multiple alwaysApply unmapped rules all appear in General", async () => {
      const sectionMapping: Record<string, string[]> = {};
      const rules = [
        makeRule("rule-1", "Always content 1", { alwaysApply: true }),
        makeRule("rule-2", "Always content 2", { alwaysApply: true }),
        makeRule("rule-3", "Always content 3", { alwaysApply: true }),
      ];
      const ruleService = createMockRuleService(rules);
      const result = await compileMonolithic(ruleService, {
        ...BASE_OPTIONS,
        sectionMapping,
      });

      expect(result.content).toContain("## General");
      expect(result.content).toContain("Always content 1");
      expect(result.content).toContain("Always content 2");
      expect(result.content).toContain("Always content 3");
    });
  });

  describe("missing rules handled gracefully", () => {
    it("silently skips rule IDs in mapping that do not exist", async () => {
      const sectionMapping = {
        "Build & Test": ["existing-rule", "nonexistent-rule"],
      };
      const rules = [makeRule("existing-rule", "Existing content")];
      const ruleService = createMockRuleService(rules);

      // Should not throw
      const result = await compileMonolithic(ruleService, {
        ...BASE_OPTIONS,
        sectionMapping,
      });

      expect(result.content).toContain("Existing content");
    });

    it("section is omitted entirely if all its rules are missing", async () => {
      const sectionMapping = {
        "Build & Test": ["nonexistent-1", "nonexistent-2"],
        Architecture: ["real-rule"],
      };
      const rules = [makeRule("real-rule", "Real content")];
      const ruleService = createMockRuleService(rules);
      const result = await compileMonolithic(ruleService, {
        ...BASE_OPTIONS,
        sectionMapping,
      });

      expect(result.content).not.toContain("## Build & Test");
      expect(result.content).toContain("## Architecture");
    });
  });

  describe("dry-run mode", () => {
    it("returns dryRun: false by default", async () => {
      const ruleService = createMockRuleService([]);
      const result = await compileMonolithic(ruleService, BASE_OPTIONS);
      expect(result.dryRun).toBe(false);
    });

    it("returns dryRun: true when dryRun option is set", async () => {
      const ruleService = createMockRuleService([]);
      const result = await compileMonolithic(ruleService, {
        ...BASE_OPTIONS,
        dryRun: true,
      });
      expect(result.dryRun).toBe(true);
    });

    it("still returns compiled content in dry-run mode", async () => {
      const sectionMapping = { "Build & Test": ["rule-a"] };
      const rules = [makeRule("rule-a", "Build content")];
      const ruleService = createMockRuleService(rules);
      const result = await compileMonolithic(ruleService, {
        ...BASE_OPTIONS,
        sectionMapping,
        dryRun: true,
      });

      expect(result.dryRun).toBe(true);
      expect(result.content).toContain("Build content");
      expect(result.content).toContain(PROJECT_INSTRUCTIONS_HEADER);
    });
  });

  describe("custom section mapping", () => {
    it("uses custom section mapping instead of default", async () => {
      const customSectionMapping = {
        "My Custom Section": ["my-rule"],
      };
      const rules = [makeRule("my-rule", "My custom content")];
      const ruleService = createMockRuleService(rules);
      const result = await compileMonolithic(ruleService, {
        ...BASE_OPTIONS,
        sectionMapping: customSectionMapping,
      });

      expect(result.content).toContain("## My Custom Section");
      expect(result.content).toContain("My custom content");
      // Default sections should NOT appear
      expect(result.content).not.toContain("## Build & Test");
      expect(result.content).not.toContain("## Code Style");
    });

    it("custom mapping with multiple sections produces all sections", async () => {
      const customSectionMapping = {
        "Section One": ["rule-one"],
        "Section Two": ["rule-two"],
      };
      const rules = [makeRule("rule-one", "Content one"), makeRule("rule-two", "Content two")];
      const ruleService = createMockRuleService(rules);
      const result = await compileMonolithic(ruleService, {
        ...BASE_OPTIONS,
        sectionMapping: customSectionMapping,
      });

      expect(result.content).toContain("## Section One");
      expect(result.content).toContain("## Section Two");
      expect(result.content).toContain("Content one");
      expect(result.content).toContain("Content two");
    });
  });

  describe("rulesSkipped tracking", () => {
    it("rules not included in any section or General are listed in rulesSkipped", async () => {
      const sectionMapping = {
        "Build & Test": ["included-rule"],
      };
      const rules = [makeRule("included-rule", "Included"), makeRule("skipped-rule", "Skipped")];
      const ruleService = createMockRuleService(rules);
      const result = await compileMonolithic(ruleService, {
        ...BASE_OPTIONS,
        sectionMapping,
      });

      expect(result.rulesSkipped).toContain("skipped-rule");
      expect(result.rulesSkipped).not.toContain("included-rule");
    });

    it("alwaysApply rules in General are NOT in rulesSkipped", async () => {
      const sectionMapping: Record<string, string[]> = {};
      const rules = [
        makeRule("always-rule", "Always content", { alwaysApply: true }),
        makeRule("manual-rule", "Manual content"),
      ];
      const ruleService = createMockRuleService(rules);
      const result = await compileMonolithic(ruleService, {
        ...BASE_OPTIONS,
        sectionMapping,
      });

      expect(result.rulesSkipped).not.toContain("always-rule");
      expect(result.rulesSkipped).toContain("manual-rule");
    });

    it("rulesSkipped is empty when all rules are included", async () => {
      const sectionMapping = {
        "Build & Test": ["rule-a", "rule-b"],
      };
      const rules = [makeRule("rule-a", "Content A"), makeRule("rule-b", "Content B")];
      const ruleService = createMockRuleService(rules);
      const result = await compileMonolithic(ruleService, {
        ...BASE_OPTIONS,
        sectionMapping,
      });

      expect(result.rulesSkipped).toHaveLength(0);
    });

    it("tracks multiple skipped rules correctly", async () => {
      const sectionMapping = {
        "Build & Test": ["included-1"],
      };
      const rules = [
        makeRule("included-1", "Included"),
        makeRule("skipped-1", "Skipped 1"),
        makeRule("skipped-2", "Skipped 2"),
        makeRule("skipped-3", "Skipped 3"),
      ];
      const ruleService = createMockRuleService(rules);
      const result = await compileMonolithic(ruleService, {
        ...BASE_OPTIONS,
        sectionMapping,
      });

      expect(result.rulesSkipped).toHaveLength(3);
      expect(result.rulesSkipped).toContain("skipped-1");
      expect(result.rulesSkipped).toContain("skipped-2");
      expect(result.rulesSkipped).toContain("skipped-3");
    });
  });

  describe("empty sections omitted", () => {
    it("section with no matching rules does not appear in output", async () => {
      const sectionMapping = {
        "Empty Section": ["nonexistent-rule"],
        "Non-empty Section": ["real-rule"],
      };
      const rules = [makeRule("real-rule", "Real content")];
      const ruleService = createMockRuleService(rules);
      const result = await compileMonolithic(ruleService, {
        ...BASE_OPTIONS,
        sectionMapping,
      });

      expect(result.content).not.toContain("## Empty Section");
      expect(result.content).toContain("## Non-empty Section");
    });

    it("General section does not appear when no unmapped alwaysApply rules exist", async () => {
      const sectionMapping = {
        "Build & Test": ["rule-a"],
      };
      const rules = [makeRule("rule-a", "Content A")];
      const ruleService = createMockRuleService(rules);
      const result = await compileMonolithic(ruleService, {
        ...BASE_OPTIONS,
        sectionMapping,
      });

      expect(result.content).not.toContain("## General");
    });

    it("output with no matching rules has no section headers", async () => {
      const sectionMapping = {
        "Section A": ["nonexistent-a"],
        "Section B": ["nonexistent-b"],
      };
      const ruleService = createMockRuleService([]);
      const result = await compileMonolithic(ruleService, {
        ...BASE_OPTIONS,
        sectionMapping,
      });

      expect(result.content).not.toContain("## Section A");
      expect(result.content).not.toContain("## Section B");
      expect(result.content).not.toContain("## General");
      // But the header and comment should still be there
      expect(result.content).toContain(PROJECT_INSTRUCTIONS_HEADER);
    });
  });

  describe("rulesIncluded tracking", () => {
    it("included section rules appear in rulesIncluded", async () => {
      const sectionMapping = {
        "Build & Test": ["rule-a", "rule-b"],
      };
      const rules = [makeRule("rule-a", "Content A"), makeRule("rule-b", "Content B")];
      const ruleService = createMockRuleService(rules);
      const result = await compileMonolithic(ruleService, {
        ...BASE_OPTIONS,
        sectionMapping,
      });

      expect(result.rulesIncluded).toContain("rule-a");
      expect(result.rulesIncluded).toContain("rule-b");
    });

    it("alwaysApply unmapped rules appear in rulesIncluded", async () => {
      const sectionMapping: Record<string, string[]> = {};
      const rules = [makeRule("always-rule", "Always content", { alwaysApply: true })];
      const ruleService = createMockRuleService(rules);
      const result = await compileMonolithic(ruleService, {
        ...BASE_OPTIONS,
        sectionMapping,
      });

      expect(result.rulesIncluded).toContain("always-rule");
    });
  });
});
